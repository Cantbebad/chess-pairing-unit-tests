	test_generate_for_2_players(t) {
		let res = generateBergerPairingsIdx(2)

		t.mini.assertEq(res.length, 1)
		t.mini.assertEq(res[0].length, 1)
		t.mini.assertEq(res[0][0], [0, 1], t.cmp_arr_items_eq)
	}

	test_generate_for_3_players_should_throw(t) {
		// generates wrong data:
			// [ [ [ 0, 2 ] ], [ [ 2, 0 ] ] ]
		// should throw OR
		// rewrite algo as:
		//   add one player (will have idx === players.length-1)
		//   generate pairing
		//   remove all pairing with that player
		// OR (better):
		// rewrite algo as:
		//   add one player (will have idx === players.length-1)
		//   do not add pairing with that player to output, if possible

		try {
			let res = generateBergerPairingsIdx(3)
			t.fn_fail()
		}
		catch {
			t.fn_ok()
		}

	}

	test_generate_for_4_players(t) {
		let res = generateBergerPairingsIdx(4)

		const expected = [
			  [ [ 0, 3 ], [ 1, 2 ] ],
			  [ [ 3, 2 ], [ 0, 1 ] ],
			  [ [ 1, 3 ], [ 2, 0 ] ]
			]

		t.mini.assertEq(res.length, 3)
		for (let i=0; i<3; ++i) {
			for (let y=0; y<2; ++y) {
				t.mini.assertEq(res[i][y], expected[i][y], t.cmp_arr_items_eq)
			}
		}
	}

